// IF NUMBER IS EVEN OR ODD, CHK IF NUMBER IS MULTIPLE OF 2

 int main()
{
    int n, x, y, z;
    cout << "Enter Random Value:-  \n";

    cin >> n >> x >> y >> z;

    if (n % 2 == 0)

        cout << "...Given Number is Even...\n"
             << n;

    else
        cout << "...Given Number is Odd\n"
             << n;

    if (x % 2 == 0)

        cout << "...Given Number is Even...\n"
             << x;

    else
        cout << "...Given Number is Odd\n"
             << x;

    if (y % 2 == 0)

        cout << "...Given Number is Even...\n"
             << y;

    else
        cout << "...Given Number is Odd\n"
             << y;

    if (z % 2 == 0)

        cout << "...Given Number is Even...\n"
             << z;

    else
        cout << "...Given Number is Odd\n"
             << z;

    return 0;
}

