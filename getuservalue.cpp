#include <iostream>
using namespace std;

// GETING TWO VALUES FROM USER TO GET THIRD VALUE

int main()
{
    int a, b, c;

    cout << "Enter the value of a: " << endl;
    cin >> a;

    cout << "Enter the value of b: " << endl;
    cin >> b;

    c = a * b;

    cout << "------------------" << endl;
    cout << "Value of c = " << c << endl;

    return 0;
}
